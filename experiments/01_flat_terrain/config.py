"""
Hyperparameter configuration for flat-terrain AnymalC velocity-tracking experiment.

All magic numbers live here. No numeric literals should appear in train.py.
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class RewardWeights:
    """Per-term reward scaling factors."""

    lin_vel_tracking: float = 1.0
    ang_vel_tracking: float = 0.5
    lin_vel_z_penalty: float = -2.0
    ang_vel_xy_penalty: float = -0.05
    joint_torque_penalty: float = -1.0e-5
    joint_accel_penalty: float = -2.5e-7
    action_rate_penalty: float = -0.01
    feet_air_time_bonus: float = 0.25
    undesired_contact_penalty: float = -1.0
    flat_orientation_penalty: float = -5.0


@dataclass
class PPOConfig:
    """RSL-RL PPO algorithm hyperparameters."""

    learning_rate: float = 1e-3
    gamma: float = 0.99
    gae_lambda: float = 0.95
    clip_param: float = 0.2
    # Entropy bonus coefficient — keeps policy from collapsing to deterministic
    entropy_coef: float = 0.01
    # Value function loss coefficient
    value_loss_coef: float = 1.0
    # Max gradient norm for clipping
    max_grad_norm: float = 1.0
    # Number of PPO epochs per rollout batch
    num_learning_epochs: int = 5
    # Number of mini-batches per epoch
    num_mini_batches: int = 4
    # Rollout horizon (steps per env before an update)
    num_steps_per_env: int = 24


@dataclass
class NetworkConfig:
    """Actor-critic MLP architecture."""

    actor_hidden_dims: list[int] = field(default_factory=lambda: [512, 256, 128])
    critic_hidden_dims: list[int] = field(default_factory=lambda: [512, 256, 128])
    activation: str = "elu"
    # Initial noise std for the stochastic policy
    init_noise_std: float = 1.0


@dataclass
class TrainingConfig:
    """Top-level training configuration — single source of truth."""

    # --- Environment ---
    num_envs: int = 4096
    terrain_type: str = "flat"
    # Registered gym environment ID for AnymalC velocity tracking
    env_id: str = "Isaac-Velocity-Flat-Anymal-C-v0"
    # Observation / action space (set by the Isaac Lab task; stored here for
    # logging purposes only — do NOT hard-code into network construction).
    device: str = "cuda"

    # --- Training schedule ---
    max_iterations: int = 1500
    # Interval (in iterations) between wandb log flushes
    log_interval: int = 10
    # Interval (in iterations) between checkpoint saves
    checkpoint_interval: int = 100
    checkpoint_dir: str = "/workspace/checkpoints/flat"
    best_model_path: str = "/workspace/checkpoints/flat/best_model.pt"

    # --- Early stopping ---
    # Mean episode reward threshold that triggers early stopping
    early_stop_reward_threshold: float = 8.0
    # Number of *consecutive* iterations above threshold before stopping
    early_stop_patience: int = 50

    # --- wandb ---
    wandb_project: str = "isaac-lab-locomotion"
    wandb_run_name: str | None = None  # None → auto-generated by wandb
    wandb_tags: list[str] = field(default_factory=lambda: ["anymal-c", "flat", "ppo"])

    # --- Sub-configs ---
    ppo: PPOConfig = field(default_factory=PPOConfig)
    network: NetworkConfig = field(default_factory=NetworkConfig)
    reward_weights: RewardWeights = field(default_factory=RewardWeights)
